name = "ada lovelace"
# método .title()
# Exibe cada palavra com uma letra maiúscula no início.

print(name.title())

# métodos .upper() e .lower()
# .upper() altera toda a string para letra maiúscula
print(name.upper())

# altera toda a string para letra minúscula
print(name.lower())

# concatenação
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
print(full_name.title())

# \t
# acrescenta espaço em branco
print("\t", name)
print("\tPiranha")

# \n
# acrescenta quebra de linha em uma string
print("\nAda")
